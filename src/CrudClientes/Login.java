    public void loginAction(){
       String login = jTLogin.getText();
       String senha = JtSenha.getText();
       System.out.println(senha);
       
       if(!"admin".equals(login) && !"admin".equals(senha)){
           JOptionPane.showMessageDialog(this, "Usu치rio ou senha incorretos!", "Erro", JOptionPane.ERROR_MESSAGE);
         
       }else{
          if(!"admin".equals(login)){
              JOptionPane.showMessageDialog(this, "Usu치rio ou senha incorretos!", "Erro", JOptionPane.ERROR_MESSAGE);
          }
          if(!"admin".equals(senha)){
              JOptionPane.showMessageDialog(this, "Usu치rio ou senha incorretos!", "Erro", JOptionPane.ERROR_MESSAGE);
          }
          if("admin".equals(login) && "admin".equals(senha)){
                this.dispose();
                new Principal().setVisible(true);
          }
 
       }
    }
